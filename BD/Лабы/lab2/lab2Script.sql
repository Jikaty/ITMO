SELECT Н_ЛЮДИ.ИД, concat(Н_ЛЮДИ.ФАМИЛИЯ,' ' ,Н_ЛЮДИ.ИМЯ, ' ' ,Н_ЛЮДИ.ОТЧЕСТВО) as ФИО, round(avg(CAST(Н_ВЕДОМОСТИ.ОЦЕНКА  AS NUMERIC))) as СР_ОЦЕНКА FROM Н_УЧЕНИКИ
JOIN Н_ВЕДОМОСТИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ВЕДОМОСТИ.ЧЛВК_ИД JOIN Н_ЛЮДИ ON Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
WHERE Н_УЧЕНИКИ.ГРУППА = '4100' and Н_ВЕДОМОСТИ.ОЦЕНКА IN('2', '3', '4', '5')
GROUP BY Н_ЛЮДИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО
HAVING round(avg(CAST(Н_ВЕДОМОСТИ.ОЦЕНКА AS NUMERIC))) = (SELECT max(CAST(Н_ВЕДОМОСТИ.ОЦЕНКА AS NUMERIC)) FROM Н_ВЕДОМОСТИ
JOIN Н_УЧЕНИКИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ВЕДОМОСТИ.ЧЛВК_ИД WHERE Н_УЧЕНИКИ.ГРУППА = '1100' and Н_ВЕДОМОСТИ.ОЦЕНКА IN('2', '3', '4', '5'))

-------------------------------------------------------- 6

SELECT Н_УЧЕНИКИ.ГРУППА,Н_ЛЮДИ.ИД, concat(Н_ЛЮДИ.ФАМИЛИЯ, ' ',Н_ЛЮДИ.ИМЯ, ' ',Н_ЛЮДИ.ОТЧЕСТВО) as ФИО, Н_ВЕДОМОСТИ.НОМЕР_ДОКУМЕНТА, Н_УЧЕНИКИ.СОСТОЯНИЕ FROM Н_ПЛАНЫ
JOIN Н_УЧЕНИКИ ON Н_ПЛАНЫ.ПЛАН_ИД = Н_УЧЕНИКИ.ПЛАН_ИД
JOIN Н_ВЕДОМОСТИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ВЕДОМОСТИ.ЧЛВК_ИД
JOIN Н_ЛЮДИ ON Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
WHERE Н_ПЛАНЫ.ФО_ИД IN (1,3) and (Н_УЧЕНИКИ.НАЧАЛО - ((Н_ПЛАНЫ.КУРС -1 ) * INTERVAL '1 year') < '2012-09-01'::DATE);


