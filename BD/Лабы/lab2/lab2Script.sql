-------------------------------------------------------- 3

SELECT Н_ЛЮДИ.ИД FROM Н_УЧЕНИКИ
JOIN Н_ЛЮДИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
WHERE (date_part('year',AGE(CURRENT_DATE,Н_ЛЮДИ.ДАТА_РОЖДЕНИЯ)) < 20 and Н_УЧЕНИКИ.ГРУППА = '3102');

-------------------------------------------------------- 4

SELECT Н_ЛЮДИ.ИМЯ, count(Н_ЛЮДИ.ИМЯ) as количество FROM Н_ФОРМЫ_ОБУЧЕНИЯ
JOIN Н_ПЛАНЫ ON Н_ФОРМЫ_ОБУЧЕНИЯ.ИД = Н_ПЛАНЫ.ФО_ИД
JOIN Н_УЧЕНИКИ ON Н_ПЛАНЫ.ИД = Н_УЧЕНИКИ.ПЛАН_ИД
JOIN Н_ЛЮДИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
WHERE Н_ФОРМЫ_ОБУЧЕНИЯ.ИД = 3
GROUP BY Н_ЛЮДИ.ИМЯ HAVING count(Н_ЛЮДИ.ИМЯ) > 10

